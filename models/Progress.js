const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const ProgressSchema = Schema({
  parentId: {
    type: Schema.Types.ObjectId,
    required: true,
  },
  userId: {
    type: Schema.Types.ObjectId,
    required: true,
  },
  percentComplete: {
    type: Number,
    default: 0,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model('progress', ProgressSchema);
